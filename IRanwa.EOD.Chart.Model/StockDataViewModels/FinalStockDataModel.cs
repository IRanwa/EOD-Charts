namespace IRanwa.EOD.Chart.Model;

/// <summary>
/// Final stock data model.
/// </summary>
public class FinalStockDataModel
{
    /// <summary>
    /// Gets or sets the columns.
    /// </summary>
    public List<string> Columns { get; set; }

    /// <summary>
    /// Gets or sets the row keys.
    /// </summary>
    public Dictionary<string, string> RowKeys { get; set; }

    /// <summary>
    /// Gets or sets the market cap.
    /// </summary>
    public Dictionary<string, string> MarketCap { get; set; }

    /// <summary>
    /// Gets or sets the pe rate.
    /// </summary>
    public Dictionary<string, string> PERate { get; set; }

    /// <summary>
    /// Gets or sets the pb rate.
    /// </summary>
    public Dictionary<string, string> PBRate { get; set; }

    /// <summary>
    /// Gets or sets the current ratio.
    /// </summary>
    public Dictionary<string, string> CurrentRatio { get; set; }

    /// <summary>
    /// Gets or sets the price to sale.
    /// </summary>
    public Dictionary<string, string> PriceToSale { get; set; }

    /// <summary>
    /// Gets or sets the ev ebitda.
    /// </summary>
    public Dictionary<string, string> EvEBITDA { get; set; }

    /// <summary>
    /// Gets or sets the ev sales.
    /// </summary>
    public Dictionary<string, string> EvSales { get; set; }

    /// <summary>
    /// Gets or sets the ev opening cash flow.
    /// </summary>
    public Dictionary<string, string> EvOpeningCashFlow { get; set; }

    /// <summary>
    /// Gets or sets the earning yield.
    /// </summary>
    public Dictionary<string, string> EarningYield { get; set; }

    /// <summary>
    /// Gets or sets the debit to assets.
    /// </summary>
    public Dictionary<string, string> DebitToAssets { get; set; }

    /// <summary>
    /// Gets or sets the interest coverage.
    /// </summary>
    public Dictionary<string, string> InterestCoverage { get; set; }

    /// <summary>
    /// Gets or sets the payout ratio.
    /// </summary>
    public Dictionary<string, string> PayoutRatio { get; set; }

    /// <summary>
    /// Gets or sets the roe.
    /// </summary>
    public Dictionary<string, string> ROE { get; set; }

    /// <summary>
    /// Gets or sets the roa.
    /// </summary>
    public Dictionary<string, string> ROA { get; set; }

    /// <summary>
    /// Gets or sets the roic.
    /// </summary>
    public Dictionary<string, string> ROIC { get; set; }

    /// <summary>
    /// Gets or sets the quick ratio.
    /// </summary>
    public Dictionary<string, string> QuickRatio { get; set; }

    /// <summary>
    /// Gets or sets the gross profit margin.
    /// </summary>
    public Dictionary<string, string> GrossProfitMargin { get; set; }

    /// <summary>
    /// Gets or sets the dividend yield.
    /// </summary>
    public Dictionary<string, string> DividendYield { get; set; }

    /// <summary>
    /// Gets or sets the price to cash flow.
    /// </summary>
    public Dictionary<string, string> PriceToCashFlow { get; set; }

    /// <summary>
    /// Gets or sets the price to free cash flow.
    /// </summary>
    public Dictionary<string, string> PriceToFreeCashFlow { get; set; }

    /// <summary>
    /// Gets or sets the free cash flow yield.
    /// </summary>
    public Dictionary<string, string> FreeCashFlowYield { get; set; }

    /// <summary>
    /// Gets or sets the debit to equity.
    /// </summary>
    public Dictionary<string, string> DebitToEquity { get; set; }

    /// <summary>
    /// Gets or sets the debit to ebitda.
    /// </summary>
    public Dictionary<string, string> DebitToEBITDA { get; set; }

    /// <summary>
    /// Gets or sets the cash ratio.
    /// </summary>
    public Dictionary<string, string> CashRatio { get; set; }

    /// <summary>
    /// Gets or sets the debit ratio.
    /// </summary>
    public Dictionary<string, string> DebitRatio { get; set; }

    /// <summary>
    /// Gets or sets the operating profit margin.
    /// </summary>
    public Dictionary<string, string> OperatingProfitMargin { get; set; }

    /// <summary>
    /// Gets or sets the assets turn over ratio.
    /// </summary>
    public Dictionary<string, string> AssetsTurnOverRatio { get; set; }

    /// <summary>
    /// Gets or sets the return on capital employed.
    /// </summary>
    public Dictionary<string, string> ReturnOnCapitalEmployed { get; set; }

    /// <summary>
    /// Gets or sets the ebitda margin.
    /// </summary>
    public Dictionary<string, string> EBITDAMargin { get; set; }

    /// <summary>
    /// Gets or sets the net income margin.
    /// </summary>
    public Dictionary<string, string> NetIncomeMargin { get; set; }

    /// <summary>
    /// Gets or sets the ebitda interest expense.
    /// </summary>
    public Dictionary<string, string> EBITDAInterestExpense { get; set; }
}

